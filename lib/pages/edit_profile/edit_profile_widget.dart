  Future _onUpdateData() async {
    FocusScope.of(context).requestFocus(FocusNode());
    if (_model.nameController.text.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Please enter your full name')),
      );
      return;
    }
    if (!isValidateEmail(_model.emailController.text)) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Please enter valid email')),
      );
      return;
    }

    if (_model.emailController.text != FirebaseUtil.sessionModel.email!) {
      final String response =
          await FirebaseUtil.resetEmail(_model.emailController.text);

      if (response == 'error') {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
              content:
                  Text('This email already used, Please try another email')),
        );
        return;
      }
    }

    final AuthUserInfo model = AuthUserInfo();
    model.email = _model.emailController.text;
    model.uid = FirebaseUtil.sessionModel.uid;
    model.displayName = _model.nameController.text;

    await FirebaseUtil.updateUser(model);
    Navigator.pop(context);
  }

  bool isValidateEmail(email) {
    return RegExp(
            r"^[a-zA-Z0-9.a-zA-Z0-9.!#$%&'*+-/=?^_`{|}~]+@[a-zA-Z0-9]+\.[a-zA-Z]+")
        .hasMatch(email);
  }
